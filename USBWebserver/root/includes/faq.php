				<h1>FAQ</h1><hr>
				<h4><b>1. Apa itu Portal DiLCo?</b></h4>
				<p>Portal DiLCo merupakan website offline berisi repositori materi pembelajaran digital.
				Aplikasi ini berbasis web yang secara khusus ditujukan untuk SMK Baabul Kamil - Jatinangor.
				Website yang dikembangkan berisi memiliki UI menarik yang mampu menampilkan daftar materi berdasarkan jurusan dan mata pelajarannya.
				Adapun materi digital yang menjadi konten utama berupa video-slide sync (VSS) dalam format html.
				Selain itu, terdapat juga materi buku ajar dalam bentuk pdf.</p>
				<br>
				<h4><b>2. Siapa saja yang terlibat dalam pengembangan Portal DiLCo?</b></h4>
				<p>Portal DiLCo dapat terbentuk berkat kerjasama antara tim KKNM-PPM Integratif Unpad,
				guru-guru SMK Baabul Kamil sebagai narasumber utama, tim Dosen Pembimbing Lapangan,
				dan Asisten Penelitian di Departemen Ilmu Komputer, FMIPA, Unpad.
				Lebih rinci lagi mengenai tim pengembang, dapat dilihat pada menu <a href="index.php?about">About Us</a>.</p>
				<br>
				<h4><b>3. Kapan dan berapa lama portal DiLCo dikembangkan?</b></h4>
				<p>Portal DiLCo yang terdiri dari konten digital dan website dikembangkan selama 4 bulan masa perkuliahan semester Genap 2017/2018.
				Dimulai dari bulan April hingga akhir Juli 2018.
				Portal DiLCo ini merupakan versi 1.0 yang nantinya masih mungkin dikembangkan lebih lanjut,
				tidak hanya untuk versi offline (statis) namun memungkinkan juga versi online (dinamis).</p>
				<br>
				<h4><b>4. Kenapa perlu ada Portal DilCo?</b></h4>
				<p>Berdasarkan hasil diskusi antara tim PPM-KKNM Integratif Unpad dan SMK Baabul Kamil pada awal program PPM-KKNI, terdapat beberapa tujuan yang ingin dicapai dengan terbentuknya portal DiLCo, antara lain:<br>
				1. Portal DiLCo diperlukan sebagai sarana menampung konten pembelajaran digital yang dapat dimanfaatkan sebagai media alternatif yang mampu menunjang proses pembelajaran siswa secara mandiri.<br>
				2. Konten pembelajaran digital yang ada diharapkan dapat memperkecil gap pengetahuan antara jenjang pendidikan sebelumnya dan kurikulum yang ada di tingkat SMK.<br>
				3. Portal DiLCo diharapkan dapat menjadi sarana aktualisasi dari kreativitas mahasiswa yang tergabung di dalam tim PPM-KKNM Integratif Unpad.</p>
				<br>
				<h4><b>5. Bagaimana cara memanfaatkan atau mengakses portal DiLCo?</b></h4>
				<p>Hingga versi 1.0 ini dirilis, portal DiLCo masih bersifat offline yang disimpan dalam flash disk (portable).
				Portal DiLCo ini dapat dijalankan setelah flash disk terhubung ke perangkat laptop/komputer.
				Terdapat sebuah file index.php yang apabila diklik, akan mengarahkan Anda ke halaman browser tempat dimana interface portal DiLCo terbuka.
				Setelah interface portal DiLCo terbuka, Anda dapat menjelajahi materi pembelajaran digital yang tersedia.</p>
